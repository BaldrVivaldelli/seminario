package com.example.socket.Services.entity;

public class MessageData {


    private String origin;
    private String data;
    
	public MessageData() {
	}
	
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}

    
}
